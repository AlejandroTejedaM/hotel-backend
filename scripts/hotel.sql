-- USUARIOS
CREATE TABLE USUARIOS
(
    ID_USUARIO      NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    USERNAME        VARCHAR2(20)                  NOT NULL,
    PASSWORD        VARCHAR2(255)                 NOT NULL,
    ESTADO_REGISTRO VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT PK_USUARIOS PRIMARY KEY (ID_USUARIO),
    CONSTRAINT CHK_USUARIOS_ESTADO CHECK (ESTADO_REGISTRO IN ('ACTIVO', 'ELIMINADO')),
    CONSTRAINT CHK_USUARIOS_USERNAME_LEN CHECK (LENGTH(USERNAME) BETWEEN 5 AND 20)
);

-- ROLES
CREATE TABLE ROLES
(
    ID_ROL NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NOMBRE VARCHAR2(15) NOT NULL,

    CONSTRAINT PK_ROLES PRIMARY KEY (ID_ROL),
    CONSTRAINT UQ_ROLES_NOMBRE UNIQUE (NOMBRE)
);

-- USUARIOS_ROLES
CREATE TABLE USUARIOS_ROLES
(
    ID_USUARIO NUMBER(10) NOT NULL,
    ID_ROL     NUMBER(10) NOT NULL,

    CONSTRAINT PK_USUARIOS_ROLES PRIMARY KEY (ID_USUARIO, ID_ROL)
);

-- HUESPEDES
CREATE TABLE HUESPEDES(
    ID_HUESPED NUMBER GENERATED BY DEFAULT AS IDENTITY,

    NOMBRE VARCHAR2(50) NOT NULL,
    APELLIDO_PATERNO VARCHAR2(50) NOT NULL,
    APELLIDO_MATERNO VARCHAR2(50),
    EMAIL VARCHAR2(100) NOT NULL,
    TELEFONO VARCHAR2(10) NOT NULL,
    DOCUMENTO VARCHAR2(20) NOT NULL,
    NACIONALIDAD VARCHAR2(50) NOT NULL,
    ESTADO_REGISTRO  VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT HUESPED_PK PRIMARY KEY(ID_HUESPED),


    CONSTRAINT HUESPED_EMAIL_CHK CHECK (REGEXP_LIKE(EMAIL, '^[^@]+@[^@]+\.[^@]+$')),
    CONSTRAINT HUESPED_TELEFONO_CHK CHECK (REGEXP_LIKE(TELEFONO, '^[0-9]{10}$')),
    CONSTRAINT HUESPED_ESTADO_CHK CHECK (ESTADO_REGISTRO IN ('ACTIVO','ELIMINADO'))
    CONSTRAINT CHK_HUESPEDES_NOMBRE_LEN CHECK (LENGTH(NOMBRE) BETWEEN 2 AND 50),
    CONSTRAINT CHK_HUESPEDES_AP_PAT_LEN CHECK (LENGTH(APELLIDO_PATERNO) BETWEEN 2 AND 50),
    CONSTRAINT CHK_HUESPEDES_AP_MAT_LEN CHECK (LENGTH(APELLIDO_MATERNO) BETWEEN 2 AND 50),

)

-- HABITACIONES
CREATE TABLE HABITACIONES
(
    ID_HABITACION     NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    NUMERO            NUMBER(10)                        NOT NULL,
    TIPO              VARCHAR2(50)                      NOT NULL,
    PRECIO            NUMBER(10, 2)                     NOT NULL,
    CAPACIDAD         NUMBER(3)                         NOT NULL,
    ESTADO_HABITACION VARCHAR2(20) DEFAULT 'DISPONIBLE' NOT NULL,
    ESTADO_REGISTRO   VARCHAR2(20) DEFAULT 'ACTIVO'     NOT NULL,

    CONSTRAINT PK_HABITACIONES PRIMARY KEY (ID_HABITACION),
    CONSTRAINT CHK_HABITACIONES_ESTADO_HAB CHECK (ESTADO_HABITACION IN
                                                  ('DISPONIBLE', 'OCUPADA', 'LIMPIEZA', 'MANTENIMIENTO')),
    CONSTRAINT CHK_HABITACIONES_ESTADO_REGISTRO CHECK (ESTADO_REGISTRO IN ('ACTIVO', 'ELIMINADO')),
    CONSTRAINT CHK_HABITACIONES_PRECIO CHECK (PRECIO > 0),
    CONSTRAINT CHK_HABITACIONES_CAPACIDAD CHECK (CAPACIDAD >= 1),
    CONSTRAINT CHK_HABITACIONES_NUMERO CHECK (NUMERO > 0)
);

-- RESERVACIONES
CREATE TABLE RESERVACIONES
(
    ID_RESERVACION  NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
    ID_HUESPED      NUMBER(10)                        NOT NULL,
    ID_HABITACION   NUMBER(10)                        NOT NULL,
    FECHA_ENTRADA   DATE                              NOT NULL,
    FECHA_SALIDA    DATE                              NOT NULL,
    ESTADO_RESERVA  VARCHAR2(20) DEFAULT 'CONFIRMADA' NOT NULL,
    ESTADO_REGISTRO VARCHAR2(20) DEFAULT 'ACTIVO'     NOT NULL,

    CONSTRAINT PK_RESERVACIONES PRIMARY KEY (ID_RESERVACION),
    CONSTRAINT CHK_RESERVACIONES_ESTADO_RESERVA CHECK (ESTADO_RESERVA IN ('CONFIRMADA', 'EN_CURSO', 'FINALIZADA', 'CANCELADA')),
    CONSTRAINT CHK_RESERVACIONES_ESTADO_REGISTRO CHECK (ESTADO_REGISTRO IN ('ACTIVO', 'ELIMINADO')),
    CONSTRAINT CHK_RESERVACIONES_FECHAS CHECK (FECHA_ENTRADA < FECHA_SALIDA)
);

